public class Direction {
    public double x;
    public double y;

    public Direction(double inputX, double inputY) {
        x = inputX;
        y = inputY;
    }
    public void set(double inputX, double inputY) {
        x = inputX;
        y = inputY;
    }

    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
}
