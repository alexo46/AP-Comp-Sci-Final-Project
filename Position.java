public class Position {
        public double x;
        public double y;
    
        // constructor that initalizes the x and y variables 
        public Position(double inputX, double inputY) {
            x = inputX;
            y = inputY;
        }
        
        // returns x variable
        public double getX() {
            return x;
        }
        
        // returns y variable
        public double getY() {
            return y;
        }
}
